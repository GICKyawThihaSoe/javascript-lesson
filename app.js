let color = "red";

switch(color){
    case "green":
        console.log("It is green");
        break;
    case "blue":
        console.log("It is blue");
        break;
    case "yellow":
        console.log("It is yellow");
        break;
    case "red":
        console.log("It is red");
        break;
    default:
        console.log("I can't guess!");
}