// Conditional Operators
// ==, ===, >, <, >=, <=, !=

let x = 20;
let y = 20;
let a = x == y;
let b = x === y;
let c = x > y;
let d = x < y;
let e = x >= y;
let f = x <= y;
let g = x != y;

console.log(a);
console.log(b);
console.log(c);
console.log(d);
console.log(e);
console.log(f);
console.log(g);

let ab = 20;
let cd = "20";
let ac = ab === cd; // === (type,value)
let bd = ab == cd; // == (value)
console.log(ac);
console.log(bd);
